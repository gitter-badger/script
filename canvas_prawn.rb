## FEATURES
# Vector drawing support, including lines, polygons, curves, ellipses.
# Extensive text rendering support (flowing text and limited inline).
# Support for both PDF builtin fonts as well as embedded TrueType fonts.
# Layout tools, including a simple grid system.
# PNG and JPG image embedding, with flexible scaling options.
# Reporting generation for data tables, with pagination support.
# Security features including encryption and password protection.
# Rendering repeatable content (i.e headers, footers, and page numbers)
# Internationalization features:
  # full support for UTF-8 based fonts,
  # right-to-left text rendering,
  # fallback font support,
  # extension points for customizable text wrapping.
# Support for PDF outlines for document navigation.
# Create custom extensions:
  # By dropping down all the way to the PDF object tree layer.
  # Mostly useful to those with knowledge of the PDF specification.

